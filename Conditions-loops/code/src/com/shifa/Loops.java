package com.shifa;

import java.util.Scanner;

public class Loops {
    public static void main(String[] args) {
          /*
            Syntax for loops :
            for(initialization; condition; increment/decrement) {
            //body
            }
           */
        // Q: Print numbers from 1 to 5
        for (int num = 1; num <= 5; num += 1) {
            System.out.println(num);
        }
    }
}
